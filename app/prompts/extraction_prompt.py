class ExtractionPrompt:
    @staticmethod
    def build(text: str, user_request: str, max_chars: int = 12000) -> str:
        document_text = text[:max_chars]

        return f"""
Ты — локальный AI-агент для анализа документов.

Извлеки из документа информацию по запросу пользователя.

Запрос пользователя:
{user_request}

Правила:
- Отвечай только на русском языке.
- Не выдумывай данные.
- Если данных нет в документе, укажи это в блоке "Не найдено".
- По возможности указывай источник: страница, раздел или фрагмент.
- Ответь строго в заданном формате.

Ответь только валидным JSON без Markdown.

Схема:
{{
  "items": [
    {{
      "field": "...",
      "value": "...",
      "source": "...",
      "confidence": "высокая/средняя/низкая"
    }}
  ],
  "missing": ["..."],
  "notes": ["..."]
}}

Документ:
{document_text}
"""
